package random;

public class DifferenceSingleAndDoubleQuote {
    public static void main(String[] args) {
        //in java single quote means character and double quote is string

        //if we add two string then the strings get concatenated.
        System.out.println("H" + "i");

        //if we add two characters then the ASCII value of those characters gets added.
        System.out.println('H' + 'i');
    }
}
